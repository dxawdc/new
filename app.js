// ==========================================
// 🎲 核心方法：根据页面路由随机抽取文案
// ==========================================
const getShareTitleByRoute = (route) => {
  // 如果能匹配到当前路由，就用对应数组；匹配不到就用 default 数组
  const titles = PAGE_SHARE_TITLES[route] || PAGE_SHARE_TITLES['default'];
  const randomIndex = Math.floor(Math.random() * titles.length);
  return titles[randomIndex];
};

// ==========================================
// 🚀 统一拦截层 (您的统一处理逻辑)
// ==========================================
const originalPage = Page;
Page = function (pageConfig) {
  
  // 1. 全局配置：发送给朋友
  if (!pageConfig.onShareAppMessage) {
    pageConfig.onShareAppMessage = function () {
      // this.route 是微信小程序原生自带的属性，能直接获取当前页面的路径
      return {
        title: getShareTitleByRoute(this.route), 
        path: '/pages/index/index', // 统一引流到首页
        //imageUrl: '' // 可选：配置一张好玩的统一分享图
      };
    };
  }
  
  // 2. 全局配置：分享到朋友圈
  if (!pageConfig.onShareTimeline) {
    pageConfig.onShareTimeline = function () {
      return {
        title: getShareTitleByRoute(this.route),
        query: ''
      };
    };
  }
  
  // 执行原本的页面初始化逻辑
  return originalPage(pageConfig);
};

